// Adding Two Numbers
// Description: It reads out the addtion of two numbes 
// Input: Double 
// Output: Double 
// Example: 1+1=2
public class Addnum
{ 
    private double num;
    
    public Addnum(double num1, double num2)
    {
        num = num1 + num2;
        System.out.println("Sum of two numbers =" + num);
    }
}
